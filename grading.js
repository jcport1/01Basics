// Grade problem

// students marks : 
// 10 - Amazing
// 5 - Good
// 3 - poor 
// 0 - fail

var grade = 3

if (grade === 10) {
    console.log('Amazing')
} 
else if (grade === 5) {
    console.log('Good')
}
else if (grade === 3) {
    console.log('poor') 
}
else if (grade === 0) {
    console.log('fail')
}